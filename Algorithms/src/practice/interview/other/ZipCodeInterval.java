package practice.interview.other;

public class ZipCodeInterval {
    private int start;
    private int end;
    ZipCodeInterval() {
        start = 0;
        end = 0;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
